void	ft_sort_int_tab(int *tab, int size)
{
	int	a;
	int	b;

	b = 0;
	while (b < (size - 1))
	{
		if (tab[b] > tab[b + 1])
		{
			a = tab[b];
			tab[b] = tab[b + 1];
			tab[b + 1] = a;
			b = 0;
		}
		else
			b++;
	}
}
